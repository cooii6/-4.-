# Маємо множину символів {c, d}.
# Додати до цієї множини множину голосних латинських літер {a, e, i, o, u, y}

def add_vowels():
    A = {"c", "d"}
    vowels = {"a", "e", "i", "o", "u", "y"}

    print("Початкова множина:", A)

    A.update(vowels)

    print("Множина після додавання голосних літер:", A)
    print("Впорядкований вивід:", sorted(A))

    return A

add_vowels()
